def gen_all_combinations(seed: int, target_size: int) -> list[dict]:
    rng = random.Random(seed)
    items: list[dict] = []

    # Full combinatorial set (bounded).
    # Take up to 1 random particle per (counter, num) to avoid 5-10x explosion
    # but keep enough particles per counter for coverage.
    sentence_id_counter = 0
    combos: list[tuple[str, int, str, str, str, str, str]] = []
    # (counter_kanji, num, p_surf, p_read, ctx_surf, ctx_read, span_hint)

    for counter_kanji, readings in COUNTERS:
        for num, rd in readings.items():
            for p_surf, p_read in PARTICLES:
                combos.append((counter_kanji, num, p_surf, p_read))

    rng.shuffle(combos)

    for counter_kanji, num, p_surf, p_read in combos:
        # 各組について複数の文脈を付与。
        # 8-12 ctx per combo で target 40K まで水増し。
        n_ctx = rng.randint(8, 12)
        ctxs = rng.sample(CONTEXT_TEMPLATES, min(n_ctx, len(CONTEXT_TEMPLATES)))
        for ctx_r, ctx_s in ctxs:
            # target: {num}{counter}{particle}
            number_reading = COUNTERS[0][1]  # placeholder; actual readings
            # 本来の読みを counters から再取得
            reading = None
            for ck, rds in COUNTERS:
                if ck == counter_kanji and num in rds:
                    reading = rds[num]
                    break
            if reading is None:
                continue
            target_surface = f"{num}{counter_kanji}{p_surf}"
            target_reading = reading + p_read
            items.append({
                "reading": target_reading,
                "surface": target_surface,
                "left_context_surface": ctx_s,
                "left_context_reading": ctx_r,
                "span_bunsetsu": 1,
                "source": "synth_numeric",
                "sentence_id": f"synth_numeric:{sentence_id_counter}",
            })
            sentence_id_counter += 1
            if len(items) >= target_size:
                return items
    return items


def main() -> None:
    ap = argparse.ArgumentParser()
    ap.add_argument("--out", required=True)
    ap.add_argument("--target-size", type=int, default=40000)
    ap.add_argument("--seed", type=int, default=42)
    args = ap.parse_args()

    items = gen_all_combinations(args.seed, args.target_size)
    out = Path(args.out)
    out.parent.mkdir(parents=True, exist_ok=True)
    with out.open("w", encoding="utf-8") as f:
        for item in items:
            f.write(json.dumps(item, ensure_ascii=False) + "\n")

    from collections import Counter
    span_dist = Counter(i["span_bunsetsu"] for i in items)
    ctx_count = sum(1 for i in items if i["left_context_surface"])
    print(f"wrote {len(items):,} items -> {out}")
    print(f"  span_bunsetsu dist: {dict(span_dist)}")
    print(f"  with left_context:  {ctx_count:,} ({ctx_count/len(items)*100:.1f}%)")
    # Sample
    print("\n--- sample ---")
    for it in items[:8]:
        ctx = f"[{it['left_context_surface']}] " if it['left_context_surface'] else ""
        print(f"  {ctx}{it['reading']:<20}  -> {it['surface']}")


if __name__ == "__main__":
    main()
