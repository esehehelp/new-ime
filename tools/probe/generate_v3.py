# Category-matching heuristics on surface text. Items already include natural
# left context from Wikipedia, so we mainly filter by length and pattern.
NUMBER_RE = re.compile(r"[0-9０-９]|[一二三四五六七八九十百千万億]")
NAME_KEY_RE = re.compile(r"[市町村県都道府]|大学|博士|教授|株式会社|神社|寺|駅|空港|地方|陛下")
TECH_KEY_RE = re.compile(r"コンピュータ|ソフト|ハード|プログラム|データ|ネットワーク|インターネット|サーバ|アプリ|デジタル|システム|エンジン|アルゴリズム|コード|ファイル")


def _classify(item: dict) -> str | None:
    s = item["surface"]
    if NUMBER_RE.search(s):
        return "numeric"
    if TECH_KEY_RE.search(s):
        return "tech"
    if NAME_KEY_RE.search(s):
        return "names"
    return "general"


def sample_from_general(
    path: Path,
    per_category: dict[str, int],
    min_input: int = 15,
    max_input: int = 60,
    seed: int = 42,
) -> dict[str, list[dict]]:
    """Sample items from general/dev.jsonl, grouped by classified category.

    Returns: {category: [item, ...]} with original reading/surface/context.
    Only `numeric`, `names`, `tech`, `general` are sampled (hand-only for
    homophone/edge/particle — harder to filter reliably).
    """
    pool: dict[str, list[dict]] = {c: [] for c in per_category}
    with open(path, encoding="utf-8") as f:
        for line in f:
            d = json.loads(line)
            if not (min_input <= len(d["reading"]) <= max_input):
                continue
            cat = _classify(d)
            if cat is None or cat not in pool:
                continue
            pool[cat].append(d)

    rng = random.Random(seed)
    chosen: dict[str, list[dict]] = {}
    for cat, n in per_category.items():
        items = pool.get(cat, [])
        rng.shuffle(items)
        chosen[cat] = items[:n]
    return chosen


