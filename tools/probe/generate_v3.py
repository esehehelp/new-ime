def _to_katakana(hira: str) -> str:
    return jaconv.hira2kata(hira)


def build_entry(
    category: str,
    ctx: str,
    reading_hira: str,
    refs: list[str],
    idx: str,
) -> dict:
    return {
        "index": idx,
        "category": category,
        "context_text": ctx,
        "input": _to_katakana(reading_hira),
        "expected_output": list(refs),
        "original_text": refs[0],
        "splitted_input_for_limited_input_length": [],
    }


def build_probe_v3(
    general_path: Path = Path("datasets/eval/general/dev.jsonl"),
    sample_counts: dict[str, int] | None = None,
) -> list[dict]:
    if sample_counts is None:
        sample_counts = {
            "numeric": 25,
            "general": 35,
            "names": 25,
            "tech": 15,
        }
    sampled = sample_from_general(general_path, sample_counts)

    out: list[dict] = []
    per_cat_count: dict[str, int] = {c: 0 for c in HAND_BANK}

    for cat, items in HAND_BANK.items():
        for ctx, reading, refs in items:
            per_cat_count[cat] += 1
            idx = f"{cat}_{per_cat_count[cat]:03d}_hand"
            out.append(build_entry(cat, ctx, reading, refs, idx))

    for cat, items in sampled.items():
        for it in items:
            per_cat_count[cat] += 1
            idx = f"{cat}_{per_cat_count[cat]:03d}_wiki"
            out.append(build_entry(cat, it.get("context", ""), it["reading"], [it["surface"]], idx))

    return out


def main() -> None:
    import argparse
    ap = argparse.ArgumentParser()
    ap.add_argument("--out", default="datasets/eval/probe/probe.json")
    ap.add_argument("--general", default="datasets/eval/general/dev.jsonl")
    ap.add_argument("--min-bytes", type=int, default=100 * 1024)
    args = ap.parse_args()

    entries = build_probe_v3(general_path=Path(args.general))

    # category distribution + context ratio + length stats
    from collections import Counter
    cats = Counter(e["category"] for e in entries)
    with_ctx = sum(1 for e in entries if e["context_text"])
    input_lens = [len(e["input"]) for e in entries]

    out_path = Path(args.out)
    out_path.parent.mkdir(parents=True, exist_ok=True)
    text = json.dumps(entries, ensure_ascii=False, indent=2)
    out_path.write_text(text, encoding="utf-8")

    size = out_path.stat().st_size
    print(f"wrote {out_path}  {size:,} bytes  ({size/1024:.1f} KiB)")
    print(f"total items: {len(entries)}")
    print(f"category dist: {dict(cats)}")
    print(f"with context: {with_ctx} ({with_ctx/len(entries)*100:.1f}%)")
    print(f"input len: min={min(input_lens)} max={max(input_lens)} mean={sum(input_lens)//len(input_lens)}")

    ok = True
    if size < args.min_bytes:
        print(f"!! size {size} < min {args.min_bytes}")
        ok = False
    if with_ctx / len(entries) < 0.35:
        print(f"!! context ratio {with_ctx/len(entries):.2f} < 0.35")
        ok = False
    if sum(input_lens) / len(input_lens) < 15:
        print(f"!! mean input len {sum(input_lens)/len(input_lens):.1f} < 15")
        ok = False
    if not ok:
        raise SystemExit(1)


if __name__ == "__main__":
    main()
