"""Benchmark loaders. Returns list of dicts with keys:
    reading: str (hiragana for our format)
    context: str
    references: list[str] (one or more acceptable surface strings)
    source: str
"""

from __future__ import annotations

import json
import random
from pathlib import Path

import jaconv


def load_eval_v3(path: str) -> list[dict]:
    """Our eval_v3 jsonl: {reading, surface, context, source}."""
    items: list[dict] = []
    with open(path, encoding="utf-8") as f:
        for line in f:
            d = json.loads(line)
            items.append(
                {
                    "reading": d["reading"],
                    "context": d.get("context", ""),
                    "references": [d["surface"]],
                    "source": d.get("source", "eval_v3"),
                }
            )
    return items


def load_ajimee_jwtd(path: str) -> list[dict]:
    """AJIMEE-Bench JWTD_v2 evaluation_items.json.

    input is katakana, expected_output is list of acceptable surfaces.
    Convert input to hiragana so our backends can normalize it back.
    """
    with open(path, encoding="utf-8") as f:
        raw = json.load(f)
    items: list[dict] = []
    for r in raw:
        kata = r["input"]
        hira = jaconv.kata2hira(kata)
        items.append(
            {
                "reading": hira,
                "context": r.get("context_text", "") or "",
                "references": list(r["expected_output"]),
                "source": "ajimee_jwtd",
                "_index": r.get("index", ""),
            }
        )
    return items


